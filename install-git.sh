# Install the 'git-all' package which includes all sub-packages, using apt-get
sudo apt-get install git-all

# Check the installed version of Git
git version

# Set the global Git username
git config --global user.name "xxxxx"

# Set the global Git email
git config --global user.email "xxxxx@gmail.com"

# Install the GitHub CLI tool 'gh' using apt
sudo apt install gh

# Log in to GitHub using the 'gh' tool
gh auth login 